    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(estado){
            Nombre.setEditable(true);
            Apellido.setEditable(true);
            DNI.setEditable(true);
            Telefono.setEditable(true);
            
            
            Nombre.setBackground(Color.WHITE);
            Nombre.setForeground(Color.BLACK);
            Apellido.setBackground(Color.WHITE);
            Apellido.setForeground(Color.BLACK);
            DNI.setBackground(Color.WHITE);
            DNI.setForeground(Color.BLACK);
            Telefono.setBackground(Color.WHITE);
            Telefono.setForeground(Color.BLACK);
            
            
            
            estado = false;
            
        } else{
            
            Nombre.setEditable(false);
            Apellido.setEditable(false);
            DNI.setEditable(false);
            Telefono.setEditable(false);
            
            
            Nombre.setBackground(new Color(0,51,51));
            Nombre.setForeground(Color.WHITE);
            Apellido.setBackground(new Color(0,51,51));
            Apellido.setForeground(Color.WHITE);
            DNI.setBackground(new Color(0,51,51));
            DNI.setForeground(Color.WHITE);
            Telefono.setBackground(new Color(0,51,51));
            Telefono.setForeground(Color.WHITE);
            
            Mod m = new Mod();
            if (m.mod_Inq(Cod, Nombre, Apellido, DNI, Telefono)) {
                Timer timer = new Timer(2000, e -> {
                    this.dispose();
                    ((Timer) e.getSource()).stop();
                });
                timer.start();
            } else {
                
            }

            estado = true;
            
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

