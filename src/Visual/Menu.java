    private void CerrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CerrarMouseClicked
        Login b = new Login();
        int resp = JOptionPane.showConfirmDialog(null, "¬øEsta seguro de cerrar sesi√≥n?", "Alerta!", JOptionPane.YES_NO_OPTION);
        if (resp == JOptionPane.YES_OPTION) {
            this.dispose();
            java.awt.EventQueue.invokeLater(() -> {
                Login login = Login.getInstance(); // obtiene la instancia actual del Login
                login.setVisible(true);
                new Thread(() -> {
                    login.cuentaRegresiva();
                }).start();
            });
            

        }
    }//GEN-LAST:event_CerrarMouseClicked

