    public void guardar_pdf(String Inquilino, String TipoPDF, File ruta) {
        PdfDAO pa = new PdfDAO();
        PdfVO po = new PdfVO();
        po.setInquilino(Inquilino);
        po.setTipoPDF(TipoPDF);
        try {
            byte[] pdf = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(pdf);
            po.setArchivoPDF(pdf);
        } catch (IOException ex) {
            po.setArchivoPDF(null);
            //System.out.println("Error al agregar archivo pdf "+ex.getMessage());
        }
        pa.Agregar_PdfVO(po);
    }
    
    public void modificar_pdf(int Id_Pdf, String Inquilino, String TipoPDF, File ruta) {
        PdfDAO pa = new PdfDAO();
        PdfVO po = new PdfVO();
        po.setId_Pdf(Id_Pdf);
        po.setInquilino(Inquilino);
        po.setTipoPDF(TipoPDF);
        try {
            byte[] pdf = new byte[(int) ruta.length()];
            InputStream input = new FileInputStream(ruta);
            input.read(pdf);
            po.setArchivoPDF(pdf);
        } catch (IOException ex) {
            po.setArchivoPDF(null);
            //System.out.println("Error al agregar archivo pdf "+ex.getMessage());
        }
        pa.Modificar_PdfVO(po);
    }
    
