    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(estado){
            jTextId_Arr.setEditable(true);
            jTextId_Cuar.setEditable(true);
            jTextId_Serv.setEditable(true);
            jTextId_in.setEditable(true);
            
            
            jTextId_Al.setBackground(new Color(0,51,51));
            jTextId_Al.setForeground(Color.WHITE);
            jTextId_Arr.setBackground(Color.WHITE);
            jTextId_Arr.setForeground(Color.BLACK);
            jTextId_Cuar.setBackground(Color.WHITE);
            jTextId_Cuar.setForeground(Color.BLACK);
            jTextId_Serv.setBackground(Color.WHITE);
            jTextId_Serv.setForeground(Color.BLACK);
            jTextId_in.setBackground(Color.WHITE);
            jTextId_in.setForeground(Color.BLACK);
            
            
            
            estado = false;
            
        } else{
            
            jTextId_Arr.setEditable(false);
            jTextId_Cuar.setEditable(false);
            jTextId_Serv.setEditable(false);
            jTextId_in.setEditable(false);
            
            
            jTextId_Al.setBackground(new Color(0,51,51));
            jTextId_Al.setForeground(Color.WHITE);
            jTextId_Arr.setBackground(new Color(0,51,51));
            jTextId_Arr.setForeground(Color.WHITE);
            jTextId_Cuar.setBackground(new Color(0,51,51));
            jTextId_Cuar.setForeground(Color.WHITE);
            jTextId_Serv.setBackground(new Color(0,51,51));
            jTextId_Serv.setForeground(Color.WHITE);
            jTextId_in.setBackground(new Color(0,51,51));
            jTextId_in.setForeground(Color.WHITE);
            
            Mod m = new Mod();
            m.mod_Al(jTextId_Al, jTextId_in, jTextId_Arr, jTextId_Serv, jTextId_Cuar, ComboEstado);
            
            estado = true;
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

