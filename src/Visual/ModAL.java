    private void jTextId_inKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextId_inKeyTyped
        char validar = evt.getKeyChar();

        if (Character.isLetter(validar)) {
            getToolkit().beep();
            evt.consume();

            alert_int();
        }
    }//GEN-LAST:event_jTextId_inKeyTyped

